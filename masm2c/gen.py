    @staticmethod
    def isrelativejump(label: Expression) -> bool:
        return (
            isinstance(label, lark.Tree)
            and isinstance(label.children[0], lark.Tree)
            and label.children[0].data == "adddir"
            and isinstance(label.children[0].children[0], lark.Tree)
            and label.children[0].children[0].data == "ptrdir3"
            and isinstance(label.children[0].children[0].children[0], lark.Token)
            and label.children[0].children[0].children[0].type == "LABEL"
            and label.children[0].children[0].children[0].value == "dol"
        ) or (
            isinstance(label, lark.Tree)
            and isinstance(label.children[0], lark.Tree)
            and label.children[0].data == "adddir"
            and isinstance(label.children[0].children[0], lark.Tree)
            and label.children[0].children[0].data == "dollar"
        )

    def dump_globals(self) -> None:
        """Dumps the global variables to a file."""
        from masm2c.proc import Proc
        name = f"{self._namespace.lower()}.list"
        logging.info(f" *** Generating globals listing {name}")

        jsonpickle.set_encoder_options("json", indent=2)
        with open(name, "w") as lst:
            lst.write("Segment:\n")
            for segment_name in self._context.segments:
                lst.write(f"{segment_name}\n")

            lst.write("\nLabels:\n")
            for global_obj in self._context.symbols.get_globals().values():
                if isinstance(global_obj, op.label):
                    lst.write(f"{global_obj.name}\n")

            lst.write("\nProcs:\n")
            for global_obj in self._context.symbols.get_globals().values():
                if isinstance(global_obj, Proc):
                    lst.write(f"{global_obj.name} {global_obj.offset}\n")

            lst.write("\nVars:\n")
            for global_obj in self._context.symbols.get_globals().values():
                if isinstance(global_obj, op.var):
                    lst.write(f"{global_obj.name} {global_obj.offset}\n")

            lst.write(
                jsonpickle.encode((self._context.symbols.get_globals(), self._context.segments, self._context.structures)))


    def make_enums_and_labels(self, labels):
        offsets = []
        for k in labels:
            k = re.sub(r"\W", "_", k)
            offsets.append((k.lower(), self.mangle_label(k)))
        return sorted(offsets, key=lambda t: t[1])

