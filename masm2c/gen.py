def guess_int_size(v: int) -> int:
    """It returns the number of bytes needed to store the given integer.

    :param v: integer
    :return: The size of the integer.
    """
    size = 0
    if v < 0:
        v = -2 * v - 1
    if v < 2**8:
        size = 1
    elif v < 2**16:
        size = 2
    elif v < 2**32:
        size = 4
    else:
        logging.error("too big number %d", v)

    logging.debug("guess_int_size %d", size)
    return size


